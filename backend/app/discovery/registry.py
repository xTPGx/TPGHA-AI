logger = logging.getLogger("tpg.discovery.registry")

DISCOVERED_FILE = "discovered.yaml"
_LIST_SECTIONS = ["device_aliases", "cameras", "locks", "speakers", "displays",
                  "climate", "security_sensors", "personal_devices", "avoid",
                  "ignored"]


def _overlay_path() -> Path:
    return get_settings().config_path / DISCOVERED_FILE


def read_overlay() -> dict[str, Any]:
    path = _overlay_path()
    if not path.exists():
        return {}
    with path.open("r", encoding="utf-8") as fh:
        return yaml.safe_load(fh) or {}


def write_overlay(data: dict[str, Any]) -> None:
    path = _overlay_path()
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as fh:
        fh.write("# Generated by TPG HomeAI discovery approvals. Safe to edit, "
                 "but it is merged into devices.yaml at load time.\n")
        yaml.safe_dump(data, fh, sort_keys=False, allow_unicode=True)


def _slug(entity_id: str) -> str:
    base = entity_id.split(".", 1)[-1]
    return re.sub(r"[^a-z0-9_]+", "_", base.lower()).strip("_") or "device"


def _humanize_slug(value: str) -> str:
    return re.sub(r"\s+", " ", value.replace("_", " ")).strip().title()


def _room_name(room_id: str) -> str:
    try:
        from ..config_loader import get_config

        for room in get_config().devices.rooms:
            if room.id == room_id:
                return room.name
    except Exception:  # noqa: BLE001 - discovery should still write overlay
        pass
    return _humanize_slug(room_id)


def _derive_device_name(entity_id: str, friendly: str = "") -> str:
    text = f"{entity_id} {friendly}".lower()
    mobile_match = re.search(r"(tpg[ _-]*)?(iphone|ipad|watch|android)[ _-]*([a-z0-9]+)?", text)
    if mobile_match:
        prefix = "TPG " if mobile_match.group(1) else ""
        kind = mobile_match.group(2)
        suffix = mobile_match.group(3) or ""
        display_kind = {"iphone": "iPhone", "ipad": "iPad", "android": "Android",
                        "watch": "Watch"}.get(kind, kind.title())
        return f"{prefix}{display_kind}{suffix}"
    if entity_id.startswith("person."):
        return friendly or _humanize_slug(entity_id.split(".", 1)[-1])
    if friendly:
        words = friendly.split()
        if len(words) >= 2 and words[0].lower() == words[1].lower():
            return words[0].title()
        if words:
            return words[0] if len(words) > 2 else friendly
    return _humanize_slug(entity_id.split(".", 1)[-1])


def _semantic_category(entity_id: str, domain: str, category: str = "",
                       friendly: str = "") -> str:
    text = f"{entity_id} {friendly} {category}".lower()
    if is_browser_mod_entity(entity_id, friendly):
        role = browser_mod_role(HAEntity(
            entity_id=entity_id,
            state="unknown",
            friendly_name=friendly,
            domain=domain,
            available=False,
        ))
        return "browser_mod_panel" if role in {"panel_screen", "panel_speaker"} else "browser_mod_diagnostic"
    if category == "personal_device" and domain in {"sensor", "binary_sensor"}:
        return "personal_device_sensor"
    if category == "personal_device" or domain == "device_tracker":
        return "personal_device"
    if category == "personal_device_sensor":
        return "personal_device_sensor"
    if domain in {"sensor", "binary_sensor"} and any(
        h in text for h in (
            "app_version", "app version", "bssid", "ssid", "sim_1", "sim 1",
            "sim_2", "sim 2", "audio_output", "audio output",
            "location_permission", "location permission", "geocoded_location",
            "geocoded location", "last_update", "last update",
        )
    ):
        return "personal_device_sensor"
    if domain == "person":
        return "person"
    if "backup" in text:
        return "system_backup"
    if any(h in text for h in ("bssid", "ssid", "connection", "router", "eero", "wan", "internet")):
        return "network"
    if any(h in text for h in (
        "app_version", "app version", "audio_output", "audio output",
        "location_permission", "location permission", "geocoded_location",
        "geocoded location", "last_update", "last update", "sim_1", "sim 1",
        "sim_2", "sim 2",
    )):
        return "diagnostic_sensor"
    return category or domain or "other"


def _suggested_mapping(category: str, domain: str) -> str:
    if domain == "light":
        return "lights"
    if domain == "fan":
        return "fans"
    if category == "person" or domain == "person":
        return "person"
    if category == "personal_device":
        return "personal_devices"
    if domain == "camera":
        return "cameras"
    if domain == "lock":
        return "locks"
    if domain == "media_player":
        return "speakers"
    if domain == "climate":
        return "climate"
    if domain == "binary_sensor":
        return "security_sensors"
    return "device_aliases"


def _approval_label(mapping: str, semantic: str, domain: str) -> str:
    if mapping == "lights":
        return "Approve as light"
    if mapping == "fans":
        return "Approve as fan"
    if mapping == "person":
        return "Approve as person"
    if mapping == "personal_devices":
        return "Map as personal device"
    if semantic == "browser_mod_panel":
        return "Approve as panel control"
    if semantic == "browser_mod_diagnostic":
        return "Approve as panel diagnostic"
    if semantic in {"diagnostic_sensor", "personal_device_sensor", "network", "system_backup"}:
        return "Approve as diagnostic"
    if domain == "switch":
        return "Approve as switch"
    return "Approve as status"


def _auto_approvable(domain: str, semantic: str, risk: str, duplicate: bool) -> bool:
    if duplicate or risk in {"high", "critical"}:
        return False
    if semantic in {"browser_mod_panel", "browser_mod_diagnostic"}:
        return False
    if semantic in {"diagnostic_sensor", "personal_device_sensor", "network", "system_backup"}:
        return False
    return domain in {"light", "fan", "person", "device_tracker"} or semantic == "personal_device"


