def sync_classifications(classifications: list[EntityClassification]) -> None:
    """Upsert discovery rows. Preserves user decisions (approved/ignored)."""
    with get_session() as session:
        existing = {d.entity_id: d for d in session.query(DiscoveredEntity).all()}
        for c in classifications:
            row = existing.get(c.entity_id)
            if row is None:
                row = DiscoveredEntity(entity_id=c.entity_id)
                session.add(row)
            # Keep a previously-decided status (approved/ignored) sticky.
            decided = row.status in ("approved", "ignored")
            row.domain = c.domain
            row.friendly_name = c.friendly_name
            row.category = c.suggested_category
            if not decided:
                row.room = c.likely_room or ""
                row.risk_level = c.risk_level
                row.suggested_aliases = json.dumps(c.suggested_aliases)
                row.reason = c.reason
                row.status = "known" if c.status == "known" else (
                    row.status if row.status in ("approved", "ignored") else c.status)
            row.is_available = c.is_available
        session.commit()


def get_pending() -> list[dict[str, Any]]:
    with get_session() as session:
        rows = session.query(DiscoveredEntity).filter(
            DiscoveredEntity.status == "new").all()
        return _row_dicts(rows)


def get_all() -> list[dict[str, Any]]:
    with get_session() as session:
        return _row_dicts(session.query(DiscoveredEntity).all())


def _row_dicts(rows: list[DiscoveredEntity]) -> list[dict[str, Any]]:
    all_ids = [r.entity_id for r in rows]
    return [_row_dict(r, all_ids) for r in rows]


def _row_dict(r: DiscoveredEntity, all_entity_ids: Optional[list[str]] = None) -> dict[str, Any]:
    try:
        aliases = json.loads(r.suggested_aliases) if r.suggested_aliases else []
    except json.JSONDecodeError:
        aliases = []
    semantic = _semantic_category(r.entity_id, r.domain or "", r.category or "",
                                  r.friendly_name or "")
    device_name = _derive_device_name(r.entity_id, r.friendly_name or "")
    suggested_mapping = _suggested_mapping(semantic, r.domain or "")
    duplicate = "possible duplicate" in (r.reason or "").lower()
    state = "unknown" if r.is_available else "unavailable"
    entity = HAEntity(
        entity_id=r.entity_id,
        state=state,
        friendly_name=r.friendly_name or "",
        domain=r.domain or r.entity_id.split(".", 1)[0],
        available=bool(r.is_available),
    )
    sibling_states = {
        eid: HAEntity(
            entity_id=eid,
            state="unknown",
            friendly_name="",
            domain=eid.split(".", 1)[0],
            available=False,
        )
        for eid in (all_entity_ids or [])
        if eid != r.entity_id
    }
    smart = smart_entity_name(
        r.entity_id,
        r.domain or "",
        r.friendly_name or "",
        room_id=r.room or None,
        siblings=(all_entity_ids or []),
    )
    if smart.get("aliases"):
        aliases = list(dict.fromkeys([*smart["aliases"], *aliases]))
    if (r.domain or "") == "light" and "domain is light" in str(smart.get("rename_reason") or ""):
        aliases = [a for a in aliases if " fan" not in f" {a} "]
    elif (r.domain or "") == "fan" and "domain is fan" in str(smart.get("rename_reason") or ""):
        aliases = [a for a in aliases if " light" not in f" {a} "]
    if r.room and (r.domain or "") in {"light", "fan", "switch"}:
        aliases = [a for a in aliases if a != r.room.replace("_", " ").lower()]
    health = unavailable_diagnosis(entity, sibling_states)
    browser_role = browser_mod_role(entity)
    return {
        "entity_id": r.entity_id, "status": r.status, "domain": r.domain,
        "friendly_name": r.friendly_name, "category": r.category,
        "suggested_name": smart.get("name") or r.friendly_name,
        "smart_aliases": smart.get("aliases", []),
        "rename_recommended": bool(smart.get("rename_recommended")),
        "rename_reason": smart.get("rename_reason") or "",
        "suggested_category": r.category, "semantic_category": semantic,
        "room": r.room, "likely_room": r.room,
        "device_name": device_name, "source": "Home Assistant",
        "source_detail": "Home Assistant entity state registry (/api/states)",
        "suggested_mapping": suggested_mapping,
        "approval_label": _approval_label(suggested_mapping, semantic, r.domain or ""),
        "auto_approvable": bool(r.is_available) and _auto_approvable(
            r.domain or "", semantic, r.risk_level or "low", duplicate
        ),
        "confidence": 1.0 if r.domain else 0.65,
        "risk_level": r.risk_level, "suggested_aliases": aliases,
        "reason": r.reason, "is_available": r.is_available,
        "browser_mod_role": browser_role,
        "health": health,
        "unavailable_reason": health.get("reason", "") if not r.is_available else "",
        "recommended_action": health.get("recommended_action", ""),
        "is_duplicate_candidate": duplicate,
        "ignore_reason": r.ignore_reason,
    }


def _upsert_device_alias(overlay: dict[str, Any], entry: dict[str, Any]) -> None:
    section = overlay.setdefault("device_aliases", [])
    section[:] = [e for e in section if e.get("entity_id") != entry.get("entity_id")]
    section.append(entry)


def _upsert_room_member(overlay: dict[str, Any], room: Optional[str],
                        field: str, entity_id: str) -> None:
    if not room:
        return
    rooms = overlay.setdefault("rooms", [])
    room_entry = next((r for r in rooms if isinstance(r, dict) and r.get("id") == room), None)
    if room_entry is None:
        room_entry = {"id": room, "name": _room_name(room), "aliases": [], "lights": [], "fans": []}
        rooms.append(room_entry)
    values = room_entry.setdefault(field, [])
    if entity_id not in values:
        values.append(entity_id)


# --------------------------------------------------------------------------
# Approve / ignore / map (write overlay + update DB)
# --------------------------------------------------------------------------
def approve(entity_id: str, mapping: str = "device_aliases",
            room: Optional[str] = None, friendly_name: Optional[str] = None,
            aliases: Optional[list[str]] = None) -> dict[str, Any]:
    domain = entity_id.split(".", 1)[0]
    name = friendly_name or _slug(entity_id).replace("_", " ").title()
    aliases = aliases or [name.lower()]
    overlay = read_overlay()

    entry: dict[str, Any]
    if mapping == "cameras":
        entry = {"id": _slug(entity_id), "name": name, "entity_id": entity_id,
                 "aliases": aliases}
    elif mapping == "locks":
        entry = {"id": _slug(entity_id), "name": name, "entity_id": entity_id,
                 "aliases": aliases}
    elif mapping == "speakers":
        entry = {"id": _slug(entity_id), "name": name, "entity_id": entity_id,
                 "room": room, "aliases": aliases}
    elif mapping == "displays":
        entry = {"id": _slug(entity_id), "name": name, "type": "media_player",
                 "entity_id": entity_id, "aliases": aliases}
    elif mapping == "climate":
        entry = {"id": _slug(entity_id), "name": name, "entity_id": entity_id,
                 "room": room, "aliases": aliases}
    elif mapping == "security_sensors":
        entry = {"entity_id": entity_id, "name": name, "aliases": aliases}
    elif mapping == "personal_devices":
        platform = "ios" if any(k in entity_id.lower() for k in ("iphone", "ipad")) else None
        device_type = "phone" if "iphone" in entity_id.lower() else (
            "tablet" if "ipad" in entity_id.lower() else "personal_device"
        )
        entry = {"id": _slug(entity_id), "name": name, "entity_id": entity_id,
                 "owner": None, "platform": platform, "device_type": device_type,
                 "room": room, "aliases": aliases}
    elif mapping in {"lights", "fans"}:
        category = "light" if mapping == "lights" else "fan"
        entry = {"id": _slug(entity_id), "name": name, "entity_id": entity_id,
                 "domain": domain, "room": room, "category": category,
                 "aliases": aliases}
        _upsert_device_alias(overlay, entry)
        _upsert_room_member(overlay, room, mapping, entity_id)
        for key in ("avoid", "ignored"):
            if key in overlay:
                overlay[key] = [e for e in overlay[key]
                                if (e if isinstance(e, str) else e.get("entity_id")) != entity_id]
        write_overlay(overlay)
        _set_status(entity_id, "approved", room=room or "", friendly=name,
                    aliases=aliases, mapping=mapping)
        logger.info("Approved %s into %s", entity_id, mapping)
        return {"entity_id": entity_id, "mapping": mapping, "entry": entry}
    elif mapping == "person":
        entry = {"id": _slug(entity_id), "name": name, "entity_id": entity_id,
                 "domain": domain, "room": None, "category": "person",
                 "aliases": aliases}
        _upsert_device_alias(overlay, entry)
        for key in ("avoid", "ignored"):
            if key in overlay:
                overlay[key] = [e for e in overlay[key]
                                if (e if isinstance(e, str) else e.get("entity_id")) != entity_id]
        write_overlay(overlay)
        _set_status(entity_id, "approved", room="", friendly=name,
                    aliases=aliases, mapping="person")
        logger.info("Approved %s into person", entity_id)
        return {"entity_id": entity_id, "mapping": "person", "entry": entry}
    else:
        mapping = "device_aliases"
        entry = {"id": _slug(entity_id), "name": name, "entity_id": entity_id,
                 "domain": domain, "room": room, "aliases": aliases}

    section = overlay.setdefault(mapping, [])
    section[:] = [e for e in section if e.get("entity_id") != entity_id]
    section.append(entry)
    # Approving removes any prior ignore of this entity.
    for key in ("avoid", "ignored"):
        if key in overlay:
            overlay[key] = [e for e in overlay[key]
                            if (e if isinstance(e, str) else e.get("entity_id")) != entity_id]
    write_overlay(overlay)

    _set_status(entity_id, "approved", room=room or "", friendly=name,
                aliases=aliases, mapping=mapping)
    logger.info("Approved %s into %s", entity_id, mapping)
    return {"entity_id": entity_id, "mapping": mapping, "entry": entry}


def ignore(entity_id: str, reason: str = "") -> dict[str, Any]:
    overlay = read_overlay()
    avoid = overlay.setdefault("avoid", [])
    if entity_id not in avoid:
        avoid.append(entity_id)
    ignored = overlay.setdefault("ignored", [])
    ignored[:] = [e for e in ignored if (e.get("entity_id") if isinstance(e, dict) else e) != entity_id]
    ignored.append({"entity_id": entity_id, "reason": reason})
    write_overlay(overlay)
    _set_status(entity_id, "ignored", ignore_reason=reason)
    logger.info("Ignored %s (%s)", entity_id, reason or "no reason")
    return {"entity_id": entity_id, "reason": reason}


def _set_status(entity_id: str, status: str, room: str = "", friendly: str = "",
                aliases: Optional[list[str]] = None, mapping: str = "",
                ignore_reason: str = "") -> None:
    with get_session() as session:
        row = session.get(DiscoveredEntity, entity_id)
        if row is None:
            row = DiscoveredEntity(entity_id=entity_id,
                                   domain=entity_id.split(".", 1)[0])
            session.add(row)
        row.status = status
        if room:
            row.room = room
        if friendly:
            row.friendly_name = friendly
        if aliases is not None:
            row.suggested_aliases = json.dumps(aliases)
        if mapping:
            row.category = row.category or mapping
        if ignore_reason:
            row.ignore_reason = ignore_reason
        session.commit()
